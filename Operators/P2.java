public class P2 {
    public static void main(String[] args) {
        int x = 40;
        System.out.println("Pre-increment: " + (++x));
        System.out.println("Post-increment: " + (x++));
        System.out.println("Pre-decrement: " + (--x));
        System.out.println("Post-decrement: " + (x--));
    }
}
